int main()
{
    fast();

    ll n, i, x; char c;
    deque<ll>d; cin >> n;
    priority_queue < ll>p;
    for (i = 0; i < n; i++) {
        cin >> x;
        d.push_back(x);
    }
    cin >> n;
    while (n--)
    {
        cin >> c;
        if (d.size()) {
            if (c == 'L') {
                if (!d.empty()) {
                    p.push(d.front()); d.pop_front();
                }
            }
            else if (c == 'R') {
                if (!d.empty()) {
                    p.push(d.back()); d.pop_back();
                }
            }
        }
       if(c=='Q') {
            if (!p.empty()) {
                cout << p.top() << "\n"; p.pop();
            }
            else cout << -1<<"\n";
        }

    }
    return 0;
}