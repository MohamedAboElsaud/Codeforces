ll n, m, x, q, p, pp[100005], a[100005],t,win,mx,index;

int main()
{
    fast();
    cin >> t;
    while (t--) {
        set<pair<ll, ll>>st;
       
        cin >> n >> q;
      
        for (ll i = 1; i <= n; i++) { pp[i] = 0, st.insert(make_pair(0, -i)); }
        win = 1, mx = 0, index = 0;
        for (ll i = 1; i <= q; i++) {
            cin >> x >> p;
            set<pair<ll, ll>>::iterator it;
            it = st.find(make_pair(pp[x], -x));
            st.erase(it);
            pp[x] += p;
            st.insert(make_pair(pp[x], -x));
            it = --st.end();
            if (it->second != -win) {
                win = -it->second;
                mx = it->first;
                index = i;
            }
        }
        cout << index<<"\n";
    }

    return 0;
}
