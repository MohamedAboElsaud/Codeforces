{
    fast();
    ll n,mx=0,x; cin >> n;
    stack<ll>st;
    while (n--) {
        cin >> x;
       mx= max(mx, x);
       if (!st.empty()) {
           if (st.top() < x) {
               cout << "NO"; return 0;
           }
           else if (st.top() == x)st.pop();
           else st.push(x);
       }
       else st.push(x);
    }
    if (st.size() >= 2 || (!st.empty() && st.top() != mx))cout << "NO";
    else cout << "YES";
    return 0;
}