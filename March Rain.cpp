ll n, k,c,a[200005],cnt,pos;
bool chk(ll x) {
    pos = 0, cnt = 0;
    for (ll i = 1; i <= n; i++) {
        if (pos < a[i]) { 
            cnt++;
            pos = a[i] + x - 1;
            if (k == cnt) {
                 return (pos >= a[n])? true:false;
                 
            }
            if (pos >= a[n]) return true ;
        }

    }
    return false;
}
