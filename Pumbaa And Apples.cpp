using namespace std;
void fast() {
    ios::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
}
ll a[1111][1111];
int main()
{
    fast();
    ll n, i, j, m, k; cin >> n >> m >> k; char c;
    vector<ll>cl(m+1), rw(n+1);
    for (i = 1; i <= n; i++) {
        rw[i] = i;
        for (j = 1; j <= m; j++) {
            cl[j] = j;
            cin >> a[i][j];
        }
    }
    while (k--) {
        cin >> c >> n >> m;
        if (c == 'g') {
            cout << a[rw[n]][cl[m]] << '\n';
        }
        else if (c == 'c') {
            swap(cl[n], cl[m]);
        }
        else if(c == 'r') {
            swap(rw[n], rw[m]);
        }
    }
    return 0;
}