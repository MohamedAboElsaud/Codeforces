ld x[600005], v[600005], mn, mx,md,n,l,r; ll i;

bool chk(double md) {
    mx = 2e9+5, mn = 2e-9-5;
    for (i = 1; i <= n; i++) {
        mx = min(mx, x[i] + md * v[i]);
        mn = max(mn, x[i] - md * v[i]);
        if (mx <= mn)return 1;
    }
    return 0;
}
int main()
{
    fast();
    cin >> n;
    for (i = 1; i <= n; i++)cin >> x[i];
    for (i = 1; i <= n; i++)cin >> v[i];
    l = 0, r = 1e9 ;
    while (r - l > 1e-7) {
        md = l + (r - l) / 2.0;
        if (chk(md))l = md;
        else r = md;
    }
    cout <<fixed<<setprecision(9)<< md;
    return 0;
}
